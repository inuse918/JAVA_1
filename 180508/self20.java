import java.util.*;

class self20 {
	public static void main(String[] args) {
		System.out.print("정수를 입력하세요 : ");
		Scanner sc = new Scanner(System.in);
		int i = sc.nextInt();
		for (int a=i;a>=1;a--){
			for (int b=1;b<=a ;b++ )
			{
				System.out.print("*");
			}
		
		System.out.println("");
		}
		
	}
}